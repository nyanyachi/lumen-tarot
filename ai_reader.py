def generate_three_card_reading(card_names, question):
    mood = detect_mood(question)

    positions = ["과거", "현재", "미래"]

    mood_advice = random.choice(MOOD_ADVICES[mood])
    closing = random.choice(CLOSINGS)

    result_lines = [
        "### 루멘의 3장 AI 리딩",
        "",
        f"**질문:** {question}",
        "",
    ]

    for position, card_name in zip(positions, card_names):
        card_messages = CARD_MESSAGES.get(card_name)

        if card_messages is None:
            card_message = "이 카드는 아직 해석 데이터가 준비되지 않았지만, 지금의 흐름을 천천히 바라보라는 메시지를 전하고 있어요."
        else:
            card_message = random.choice(card_messages)

        result_lines.append(f"**[{position}] {card_name}**")
        result_lines.append(card_message)
        result_lines.append("")

    result_lines.append("### 종합 조언")
    result_lines.append(mood_advice)
    result_lines.append("")
    result_lines.append(closing)

    return "\n".join(result_lines)