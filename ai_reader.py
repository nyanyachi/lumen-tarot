def detect_mood(question):
    question = question.lower()

    mood_keywords = {
        "love": ["사랑", "연애", "관계", "짝사랑", "이별", "재회", "마음", "상대"],
        "career": ["일", "취업", "진로", "돈", "공부", "프로젝트", "직장", "사업"],
        "anxiety": ["불안", "걱정", "힘듦", "힘들", "잘 될까", "괜찮을까", "무서", "막막"],
    }

    for mood, keywords in mood_keywords.items():
        for keyword in keywords:
            if keyword in question:
                return mood

    return "general"


